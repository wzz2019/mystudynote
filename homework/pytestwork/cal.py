class Calculator:
    def add(self,a,b):
        try :
            return a+b
        except:
            return False
    def div(self,a,b):
        try :
            return a/b
        except:
            return False
    def sub(self,a,b):
        try :
            return a-b
        except:
            return False
    def mul(self,a,b):
        try :
            return a*b
        except:
            return False